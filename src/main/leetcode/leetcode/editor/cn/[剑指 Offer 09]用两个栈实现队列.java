class CQueue {
    Stack<Integer> instack;
    Stack<Integer> outstack;
    public CQueue() {
        instack = new Stack<>();
        outstack = new Stack<>();
    }

    public void appendTail(int value) {
        if (instack.size() == 0) {
            instack.push(value);
        } else {
            while (instack.size() > 0) {
                Integer pop = instack.pop();
                outstack.push(pop);
            }
            instack.push(value);
            while (outstack.size() > 0) {
                Integer pop = outstack.pop();
                instack.push(pop);
            }
        }
    }

    public int deleteHead() {
        if (instack.size() == 0) {
            return -1;
        }
        return instack.pop();
    }
}

